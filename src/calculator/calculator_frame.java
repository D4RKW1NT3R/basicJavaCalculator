    public void percentToDouble() {
        numPercent = numPercent / 100;
        strNumPercent = Double.toString(numPercent);
        if (jLabel1.getText().equals("")) {
            ansIsPercentage = false;
        }
        else {
            ansIsPercentage = true;
        }
        
        jTextField1.setText(jTextField1.getText().replaceAll(strNum,strNumPercent));
    }
    
    public void arithmetic_operation() {
        num2 = Double.parseDouble(jTextField1.getText());
        strNum2 = Double.toString(num2);
        jLabel1.setText("");
        switch (calculation) {
            
            case 1:
                //Addition
                operation = "+";
                ans = num + num2;
                jTextField1.setText(Double.toString(ans));
                break;
            case 2:
                //Subtraction
                operation = "-";
                ans = num - num2;
                jTextField1.setText(Double.toString(ans));
                break;
            case 3:
                //Multiplication
                operation = "x";
                ans = num * num2;
                jTextField1.setText(Double.toString(ans));
                break;
            case 4:
                //Division
                operation = "รท";
                ans = num / num2;
                jTextField1.setText(Double.toString(ans));
                break;
            }
        
        if (ansIsPercentage.equals(true)) {
                ans = ans * 100;
                jTextField1.setText(Double.toString(ans) + "%");
                //numIsPercentage = false;
        }
        
        jLabel1.setText(Double.toString(num) + " " + operation + " " + strNum2 + " =");
        
    }
    
    
    

